local newTweenInfo;do
	local new = TweenInfo.new;

	local cache = {};
	newTweenInfo = function(info)
		local encoded = table.concat(info, "|");
		if not cache[encoded] then
			cache[encoded] = new(table.unpack(info));
		end;
		return cache[encoded];
	end;
end;

local TweenService = game:GetService("TweenService");
local empty_tween;
local create, play, pause, cancel; do
	local tweenCreate = TweenService.Create;
	create = function(...)
		return tweenCreate(TweenService, ...);
	end;

	local obj = Instance.new("Part");
	task.defer(obj.Destroy, obj);
	empty_tween = create(obj, TweenInfo.new(), {Transparency = 0.5});
	play = empty_tween.Play;
	pause = empty_tween.Pause;
	cancel = empty_tween.Cancel;
end;

local Tween = {
	new = function(self, object, tinfo, properties)
		return setmetatable({
			_object = object,
			_tween = empty_tween,
			_tinfo = tinfo,
			_properties = properties,
		}, self);
	end,
	play = function(self)
		-- only update properties that are different than the current value
		local newproperties = {};
		for k,v in next, self._properties do
			if self._object[k] ~= v then
				newproperties[k] = v;
			end;
		end;
		-- if we aren't updating any values, don't tween
		if not next(newproperties) then
			return self;
		end;

		self._tween = create(self._object, newTweenInfo(self._tinfo), newproperties);
		play(self._tween);
		return self;
	end,
	pause = function(self)
		if self._tween then
			pause(self._tween);
		end;
		return self;
	end,
	cancel = function(self)
		if self._tween then
			cancel(self._tween);
		end;
		return self;
	end,
};Tween.__index = Tween;

local TweenPublic = {};
function TweenPublic:new(...)
	return Tween:new(...);
end;
function TweenPublic:play(...)
	return self:new(...):play();
end;

return TweenPublic;
end
