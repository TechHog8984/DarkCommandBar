local escapes = {
	a = '\a',
	b = '\b',
	f = '\f',
	n = '\n',
	r = '\r',
	t = '\t',
	v = '\v',
	['\\'] = '\\',
	['"'] = '"', -- quote
	["'"] = "'", -- single quote
};
local function parseCommands(text)
	local parsed = {};
	local buffer = "";
	local char = "";

	local length = #text;
	local index = 1;
	local function at(offset)
		offset = offset or 0;
		return text:sub(index + offset, index + offset);
	end;
	local function addBuffer()
		if #buffer > 0 then
			table.insert(parsed, buffer);
		end;
		buffer = "";
	end;

	while index <= length do
		char = at();
		if char == '"' or char == "'" then
			local start_char = char;
			index += 1;
			while index <= length do
				char = at();
				if char == '\\' then
					buffer ..= escapes[at(1)];
					index += 1;
				elseif char == start_char then
					break;
				else
					buffer ..= char;
				end;
				index += 1;
			end;
		elseif char == ' ' then
			addBuffer();
		else
			buffer ..= char;
		end;
		index += 1;
	end;
	addBuffer();

	return parsed;
end;
