require(gui.TimeDisplay.TimeDisplay)(sys);

local SEMICOLON = Enum.KeyCode.Semicolon;
local ESCAPE = Enum.KeyCode.Escape;

local UserInputService = game:GetService("UserInputService");
UserInputService.InputBegan:Connect(function(Input, game_processed_event)
	if game_processed_event then
		return;
	end;

	if Input.KeyCode then
		if Input.KeyCode == SEMICOLON then
			--sys.hideHelp();
			BoxFrame.show();
		end;
	end;
end);
end)

return DarkCommandBar