	local function updateFrame()
		local num_items_shown = math.max(0, num_items_shown - 1);
		local size_y_offset = (num_items_shown * item_size_offset_y) + (num_items_shown * item_size_padding_y);

		shown_position = UDim2.new(0, 20, 1, -72 - size_y_offset - default_frame_size_y);
		hidden_position = UDim2.new(0, -SuggestionsFrame.AbsoluteSize.X - 6, shown_position.Y.Scale, shown_position.Y.Offset);

		Tween:new(SuggestionsFrame, {0.5, 8}, {
			Size = default_frame_size + UDim2.fromOffset(0, size_y_offset),
			Position = (is_shown and shown_position) or hidden_position
