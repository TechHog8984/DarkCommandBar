		index -= 1;
		return (index * item_size_offset_y) + (index * item_size_padding_y);
	end;
	local function addItem(item)
		if table.find(items_shown, item) then
			return;
		end;

		table.insert(items_shown, item);
		num_items_shown += 1;
		local y_position_offset = getItemYPositionOffset(num_items_shown);
		item.Position = UDim2.new(-1, 0, 0, y_position_offset);
		updateFrame();
		item.Parent = ItemContainer;
		Tween:new(item, {0.5, 8}, {Position = UDim2.fromOffset(0, y_position_offset)}):play();
	end;
